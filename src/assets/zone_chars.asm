;=======================================================================================
; Zone cell chars (runtime-loaded into char offsets ZONE_GEN_BASE..+26).
;
; A zone is a 3x3 block of 8x8 cells. Each of the 9 positions (TL,T,TR,L,C,R,
; BL,B,BR) is a distinct 8x8 char: solid zone colour, grey ($0C) only on the
; OUTWARD-facing edges, so a placed zone shows a single grey border around the
; whole 24x24 block. The center cell carries the white (R/C/I) letter.
;
; The map stores the literal char per zone cell (with bit 7 set; see cell_to_char
; and the zone paint in city.asm), so no per-cell position lookup is needed.
;=======================================================================================

tiles_load_zone_cells:
        lda #<zone_cell_chars
        sta PTR2
        lda #>zone_cell_chars
        sta PTR2+1
        lda #ZONE_GEN_BASE
        sta zone_load_char
_tlzc_loop:
        lda zone_load_char
        ldx PTR2
        ldy PTR2+1
        jsr create_fcm_char
        clc
        lda PTR2
        adc #64
        sta PTR2
        bcc +
        inc PTR2+1
+       inc zone_load_char
        lda zone_load_char
        cmp #(ZONE_GEN_BASE + 27)
        bne _tlzc_loop
        rts
zone_load_char:
        .byte 0

